SlashCmdList["REVIVEMACRO"] = function()
  CastSpellByName(rezSpell); --first cast the resurrection spell, cause sometimes you can't click a corpse directly (if the soul is released)
  message_type=random(message_types);
  local r_mes1=random(tableLength(mes1[message_type]));
  local r_mes2=random(tableLength(mes2[message_type]));
  full_message=mes1[message_type][r_mes1].."%t"..mes2[message_type][r_mes2];
  if UnitIsDead("target") then c="say" SendChatMessage(full_message, c, nil, UnitName("player"));
end
end